    private void txtFiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyTyped
        try{
        txtFiltro.addKeyListener(new KeyAdapter() {
            public void keyReleased(final KeyEvent e) {
                String cadena = (txtFiltro.getText());
                txtFiltro.setText(cadena);
                repaint();
                filtro();
            }
        });
        trsFiltro = new TableRowSorter(tblDatos.getModel());
        tblDatos.setRowSorter(trsFiltro);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error, "+e,"Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtFiltroKeyTyped

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        try{
        JTable target = (JTable)evt.getSource();
        txtCodigo.setText(target.getValueAt(target.getSelectedRow(), 0).toString());
        txtNombres.setText(target.getValueAt(target.getSelectedRow(), 1).toString());
        txtApellidos.setText(target.getValueAt(target.getSelectedRow(), 2).toString());
        txtDireccion.setText(target.getValueAt(target.getSelectedRow(), 3).toString());
        ftxtTelefono.setText(target.getValueAt(target.getSelectedRow(), 4).toString());
        txtCorreo.setText(target.getValueAt(target.getSelectedRow(), 5).toString());
        cbxSexo.setSelectedItem(target.getValueAt(target.getSelectedRow(), 6).toString());
        cbxEstado.setSelectedItem(target.getValueAt(target.getSelectedRow(), 7).toString());
        cbxCiudad.setSelectedItem(target.getValueAt(target.getSelectedRow(), 8).toString());
        String foto = target.getValueAt(target.getSelectedRow(), 9).toString();
        
        iImg = new ImageIcon(((new ImageIcon(foto)).getImage()).getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), java.awt.Image.SCALE_SMOOTH));
        lblImage.setIcon(iImg);
        
        btnDelete.setEnabled(true);
        btnModificar.setEnabled(true);
        
        btnGuardar.setEnabled(false);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error, "+e,"Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tblDatosMouseClicked

