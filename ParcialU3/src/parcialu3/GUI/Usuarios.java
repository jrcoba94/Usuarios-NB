    private void txtNombresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombresKeyTyped
        try{
        char s = evt.getKeyChar();
        if(Character.isDigit(s)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null,"Error, Ingrese valores de tipo texto","Tipo de datos",JOptionPane.WARNING_MESSAGE);
        }
        if(Character.isUpperCase(s)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null,"Error, Ingrese minúsculas","Tipo de datos",JOptionPane.WARNING_MESSAGE);
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error, "+e,"Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtNombresKeyTyped

