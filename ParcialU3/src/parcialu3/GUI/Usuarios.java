    private void lblImageMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageMousePressed
        lblImage.setIcon(null);
        SelecImg = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("PNG,JPG","png","jpg");
        try{
            SelecImg.setFileFilter(filter);
        if (SelecImg.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File arch = (File) SelecImg.getSelectedFile();
            rArch = arch.getPath();
            System.out.println(rArch);
            iImg = new ImageIcon(((new ImageIcon(rArch)).getImage()).getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), java.awt.Image.SCALE_SMOOTH));
            lblImage.setIcon(iImg);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error, "+e,"Error",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_lblImageMousePressed

